export type Lang = 'en' | 'zh'
export function t(key: string, lang: Lang = 'en', ...args: string[]): string {
  let value = (translations[lang] as Record<string, string>)?.[key] ?? key
  args.forEach((arg, i) => {
    value = value.replace(`{${i}}`, arg)
  })
  return value
}
